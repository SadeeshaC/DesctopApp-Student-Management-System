    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String stID = jTextField1.getText();
        String coursesName = String.valueOf(jComboBox1.getSelectedItem());
        String sttus = jTextArea1.getText();

        if (coursesName.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Plese Select Courses Name", "Worning", JOptionPane.WARNING_MESSAGE);
        } else if (sttus.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Plese Enter Status", "Worning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                Date date = new Date();
                SimpleDateFormat Datefomat = new SimpleDateFormat("yyyy-MM-dd");

                MySQL.execute("INSERT INTO `enrollments` (`EnrollmentDate`,`Status`,`Students_StudentID`,`Courses_CoursesID`)"
                        + "VALUES('" + Datefomat.format(date) + "','" + sttus + "','" + stID + "','" + coursesMap.get(coursesName) + "');");

                LoadTable();
                reset();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String coursesName = String.valueOf(jComboBox1.getSelectedItem());
        String sttus = jTextArea1.getText();

        try {
            if (jTable1.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(this, "Plese Select Row", "Worning", JOptionPane.WARNING_MESSAGE);
            } else {
                int row = jTable1.getSelectedRow();
                String id = String.valueOf(jTable1.getValueAt(row, 0));

                MySQL.execute("UPDATE `enrollments` SET `Courses_CoursesID`='" + coursesMap.get(coursesName) + "' , `Status`='" + sttus + "' WHERE `EnrollmentID`='" + id + "'");
                LoadTable();
                reset();
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

