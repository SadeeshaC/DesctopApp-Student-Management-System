    private void LoadTable() {
        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `enrollments` INNER JOIN `courses` ON `enrollments`.`Courses_CoursesID` = `courses`.`CoursesID`\n"
                    + "WHERE `Students_StudentID`='" + this.id + "'");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> Table = new Vector<>();
                Table.add(resultSet.getString("EnrollmentID"));
                Table.add(resultSet.getString("EnrollmentDate"));
                Table.add(resultSet.getString("courses.CoursesName"));
                Table.add(resultSet.getString("Status"));

                model.addRow(Table);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

